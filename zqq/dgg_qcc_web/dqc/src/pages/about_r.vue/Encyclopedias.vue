                股东（发起人）
            </div>
            <div class="pt20 f14" style="box-sizing:border-box;padding-top:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;line-height:24px;">
                股东（发起人）：我国《公司法》在关于股份有限公司的规定中，使用率“发起人”的概念，而在关于优先责任公司的规定中却未使用“发起人”而统一使用了“股东”的概念。严格说来。发起人与股东两个概念不能等同，二者既有联系又有区别。通常情况下，发起人是指参加订立发起人协议、提出设立公司的申请、认购公司出资或股份并对公司设立承担责任的人。发起人在设立过程中受发起人协议约束，在公司成立后，才具有真正股东的身份。<br style="box-sizing:border-box;"> 然而，公司的股东却并不以发起人为限。除发起人以外，任何在公司设立阶段和公司成立后认购或受让公司出资或股份的人都可以成为公司的股东。从这个意义上看，无论是股份有限公司还是有限责任公司，均应有发起人和股东的区分。只不过在股份有限公司中，由于法律规定发起人必须认足一定比例的股份，因此发起人是公司成立后的当然股东；同理，在有限责任公司中，由于法律规定发起人都负有出资义务，所以发起人在公司成立后成为首批股东。
            </div>
