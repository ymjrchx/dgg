            <div class="f18 pb20 dash-border-bottom" style="box-sizing:border-box;border-bottom:1px dashed #CDCDCD;font-size:18px;padding-bottom:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;">
                法人
            </div>
            <p>
                法人：法人是指私法上具有权利能力的团体或者组织。而由于大陆法系公法和私法划分的传统，相应的公法上的法人和私法上的法人则是一种合乎逻辑的分类。私法上的法人是通过私法行为设立的长期存在的人的联合体或者组织体，它本身是与其全体组成人员和管理人员相互分开的实体。它本身享有权利并承担义务，通过其机关的行为取得权利并履行义务，由此发挥自己的作用并参与法律交往。也就是说，法人既可以是其成员变更与其存在没有任何关系的人的联合体，也可以是为一种目的并具有为此目的而筹集的财产所组织起来的组织体。前者如公司、合作社等，后者如基金会等。<br style="box-sizing:border-box;"> 公法人是依据公法设立并以行使国家权力为宗旨的组织。由于国家不得参加一般私法交易为宪政之一般原则，故公法人在私法领域内的作用远不及私法人。但是公法人作为政治社会中的实然存在，有时非为私的需要而参与到民事活动中来，因此各国民法不得不承认其在私法中的地位。例如，国家或者各级政府作为财产所有人、债务人，为了日常工作需要而购买商品的行为等，均离不开私法的支持。但是私法讨论最多、也最具典型意义的当然是私法人而非公法人。
                <br style="box-sizing:border-box;"> </p>
            <p>
                我国《民法总则》规定“法人是具有民事权利能力和民事行为能力，依法独立享有民事权利和承担民事义务的组织。”
            </p>
            <p> <br> </p>
            <p></p>
            <div class="f18 pb20 dash-border-bottom" style="box-sizing:border-box;border-bottom:1px dashed #CDCDCD;font-size:18px;padding-bottom:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;">
                对外投资
            </div>
            <p>
                对外投资：对外投资是指企业以现金、实物、无形资产或者购买股票、债券等有价证券方式向其他单位的投资，包括短期投资和长期投资。<br style="box-sizing:border-box;"> 短期投资是指能够随时变现、持有时间不超过一年的有价证券以及超过一年的其他投资。
                <br style="box-sizing:border-box;"> </p>
            <p>
                长期投资是指不准备随时变现、持有时间在一年以上的有价证券以及超过一年的其他投资。
            </p>
