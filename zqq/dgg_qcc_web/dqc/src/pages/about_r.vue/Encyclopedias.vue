                经营状态
            </div>
            <p style="box-sizing:border-box;padding-top:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;line-height:24px;">
                经营状态：经营状态包含营业、停业（歇业）、筹建、关闭、破产等类型。
            </p>
            <p style="box-sizing:border-box;padding-top:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;line-height:24px;"> <br> </p>
            <div class="f18 pb20 dash-border-bottom" style="box-sizing:border-box;border-bottom:1px dashed #CDCDCD;font-size:18px;padding-bottom:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;">
                企业名称
            </div>
            <p>
                企业名称：公司的名称和住所是公司登记的主要事项，也是设立公司的组织条件。<br style="box-sizing:border-box;"> 公司名称的意义主要有三：其一，公司的名称是公司成为独立民事主体的重要标志之一，是法人人格的表现。其二，公司的名称也是法人人格特定化的标志，公司可以其名称区别于其他民事主体。其三，公司的名称也是公司商誉的主要组成部分，是一种无形资产。依据《保护工业产权巴黎公约》的规定，公司的名称是工业产权保护的对象之一。
                <br style="box-sizing:border-box;"> </p>
            <p>
                我国《企业名称登记管理实施办法》第9条规定：“企业名称应当由行政区划、字号、行业、组织形式依次组成，法律、行政法规和本办法另有规定的除外。”
            </p>
