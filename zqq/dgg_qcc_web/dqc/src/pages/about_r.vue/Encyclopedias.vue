                股东
            </div>
            <div class="pt20 f14" style="box-sizing:border-box;padding-top:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;line-height:24px;">
                股东：股东是组成公司并在其中享有权利的人。凡是基于对公司的投资或基于其他的合法原因而持有公司资本的一定份额并享有股东权利的人都是公司的股东。但由于公司的类型、投资人向公司投资的时间以及取得股权的方式等不同，对股东的含义有不同的表述。一般而言，有限责任公司的股东，是指因在公司成立时认缴公司资本或在公司存续期间依法继受取得股权而对公司享有权利和承担义务的人；股份有限公司的股东，是指在设立或在公司成立后合法取得公司股份并对公司享有权利和承担义务的人。
            </div>
            <p style="box-sizing:border-box;padding-top:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;line-height:24px;"> <br> </p>
            <div class="f18 pb20 dash-border-bottom" style="box-sizing:border-box;border-bottom:1px dashed #CDCDCD;font-size:18px;padding-bottom:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;">
                统一社会信用代码
            </div>
            <div class="pt20 f14" style="box-sizing:border-box;padding-top:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;line-height:24px;">
                统一社会信用代码：社会信用代码是指按照《国务院关于批转发展改革委等部门法人和其他组织统一社会信用代码制度建设总体方案的通知》（国发〔2015〕33号）规定，由赋码主管部门给每一个法人单位和其他组织颁发的在全国范围内唯一的、终身不变的法定身份识别码。统一社会信用代码由十八位阿拉伯数字或大写英文字母（不使用I、O、Z、S、V）组成。
            </div>
            <p style="box-sizing:border-box;padding-top:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;line-height:24px;"> <br> </p>
            <div class="f18 pb20 dash-border-bottom" style="box-sizing:border-box;border-bottom:1px dashed #CDCDCD;font-size:18px;padding-bottom:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;">
                所有者权益合计
            </div>
            <div class="pt20 f14" style="box-sizing:border-box;padding-top:20px;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;line-height:24px;">
                所有者权益合计：所有者权益是指企业资产扣除负债后由所有者享有的剩余权益。公司的所有者权益又称为股东权益。所有者权益包括实收资本（或者股本）、资本公积、盈余公积和未分配利润等。<br style="box-sizing:border-box;"> （1）实收资本。实收资本是指投资者按照企业章程或合同、协议的约定，实际投入企业的资本。
                <br style="box-sizing:border-box;"> （2）资本公积。资本公积是指投资人投入或其他来源取得，而归投资人享有，属于公积金性质的资本金。它包含资本（股本）溢价和直接计入所有者权益的利得和损失等。
                <br style="box-sizing:border-box;"> （3）盈余公积。盈余公积是指企业从税后利润中提取的公积金，包括法定盈余公积和任意盈余公积。
                <br style="box-sizing:border-box;"> （4）未分配利润。未分配利润是指企业的税后利润按照规定进行分配以后的剩余部分，这部分未分配利润留存在企业，可在以后年度进行分配。资产、负债和所有者权益三大要素是构成资产负债表的基本要素，习惯上又将这三大要素成为资产负债表要素。
            </div>
            <div style="box-sizing:border-box;color:#454545;font-family:&quot;white-space:normal;background-color:#FFFFFF;">
                相关法条<br style="box-sizing:border-box;"> 第五章　所有者权益
                <br style="box-sizing:border-box;"> 第二十六条　所有者权益是指企业资产扣除负债后由所有者享有的剩余权益。公司的所有者权益又称为股东权益。
                <br style="box-sizing:border-box;"> 第二十七条　所有者权益的来源包括所有者投入的资本、直接计入所有者权益的利得和损失、留存收益等。
                <br style="box-sizing:border-box;"> 直接计入所有者权益的利得和损失，是指不应计入当期损益、会导致所有者权益发生增减变动的、与所有者投入资本或者向所有者分配利润无关的利得或者损失。
                <br style="box-sizing:border-box;"> 利得是指由企业非日常活动所形成的、会导致所有者权益增加的、与所有者投入资本无关的经济利益的流入。损失是指由企业非日常活动所发生的、会导致所有者权益减少的、与向所有者分配利润无关的经济利益的流出。&nbsp;
                <br style="box-sizing:border-box;"> 第二十八条　所有者权益金额取决于资产和负债的计量。&nbsp;
                <br style="box-sizing:border-box;"> 第二十九条　所有者权益项目应当列入资产负债表。
            </div>
